Litt335.radius <- readline(prompt="Enter the radius of the circle")
Litt335.radius <- as.integer(Litt335.radius)
Litt335.circumference <- (2*3.14*Litt335.radius)
Litt335.area <- (3.14*Litt335.radius*Litt335.radius)
